use std::fmt;

use crate::spec::Spec;

impl fmt::Display for Spec {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        writeln!(f, "Spec: {} invariant(s)", self.len())?;
        for (i, inv) in self.invariants().iter().enumerate() {
            writeln!(f, "  {}. {}", i + 1, inv)?;
        }
        Ok(())
    }
}
